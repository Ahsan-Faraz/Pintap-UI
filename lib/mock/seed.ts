// --- time helpers (anchored to "now" so relative timestamps stay fresh) ---
const DAY = 86_400_000;
const daysAgo = (n: number) => new Date(Date.now() - n * DAY).toISOString();
const daysFromNow = (n: number) => new Date(Date.now() + n * DAY).toISOString();
const hoursAgo = (n: number) =>
  new Date(Date.now() - n * 3_600_000).toISOString();

const avatar = (seed: string) =>
  `https://api.dicebear.com/7.x/initials/svg?seed=${encodeURIComponent(seed)}&backgroundColor=002E51&textColor=ffffff`;
const img = (seed: string) => `https://picsum.photos/seed/${seed}/600/600`;
const resolver = (code: string) => `${APP_URL}/l/${code}`;

function makeClicks(linkId: string, n: number, spreadDays = 21): LinkClick[] {
  const out: LinkClick[] = [];
  for (let i = 0; i < n; i++) {
    out.push({
      id: `click-${linkId}-${i}`,
      linkId,
      visitorHash: `v_${linkId}_${i % Math.max(1, Math.floor(n * 0.7))}`,
      userAgent: i % 2 ? "Mozilla/5.0 (iPhone)" : "Mozilla/5.0 (Macintosh)",
      countryCode: ["US", "GB", "DE", "CA"][i % 4],
      source: ["instagram", "whatsapp", "direct", "tiktok"][i % 4],
      clickedAt: hoursAgo(Math.floor((i / n) * spreadDays * 24) + (i % 7)),
    });
  }
  return out;
}

